/**
 * Surgical overrides to e-snack's upstream compose:
 * - unbinds postgres host port (would collide with dashboard's own pg)
 * - hides caddy behind a never-enabled profile (we use ngrok + host Caddy)
 */
function buildOverrideCompose(): string {
  return [
    "services:",
    "  postgres:",
    "    ports: !reset []",
    "  caddy:",
    '    profiles: ["disabled-by-dashboard"]',
    "",
  ].join("\n")
}

async function exists(p: string): Promise<boolean> {
  try {
    await access(p)
    return true
  } catch {
    return false
  }
}

/**
 * Deploys a client instance, yielding progress events as it runs.
 * Consumers (e.g. SSE route) enqueue each event to the response stream.
 */
export async function* deployClientInstanceStream(clientId: string): AsyncGenerator<DeployEvent> {
  const [client] = await db.select().from(clients).where(eq(clients.id, clientId)).limit(1)
  if (!client) throw new Error("Client not found")
  const [config] = await db
    .select()
    .from(clientConfig)
    .where(eq(clientConfig.clientId, clientId))
    .limit(1)
  if (!config) throw new Error("Client config not found")

  const authtoken = process.env.NGROK_AUTHTOKEN
  if (!authtoken) throw new Error("NGROK_AUTHTOKEN is not configured on the dashboard")

  if (config.initialAdminPassword.length < 12) {
    throw new Error(
      "Le mot de passe admin restaurant doit faire au moins 12 caractères pour qu'e-snack démarre en prod. Modifie-le dans la fiche client et réessaie."
    )
  }

  const slug = client.slug
  const project = `esnack-${slug}`
  const clientDir = path.join(CLIENTS_DIR, slug)
  const network = `${project}_default`

  const logs: DeployEvent[] = []
  const pushLog = (message: string) => logs.push({ type: "log", message })

  yield { type: "phase", phase: "clone", message: `Préparation de ${clientDir}` }

  // Auto-cleanup: any pre-existing containers / network / volumes from a
  // previous failed attempt would collide on name. Nuke them first.
  const cleanupCount = await forceCleanupProject(project)
  if (cleanupCount > 0) {
    yield { type: "log", message: `Nettoyage: ${cleanupCount} ressource(s) résiduelle(s) supprimée(s)` }
  }

  await mkdir(clientDir, { recursive: true })

  if (await exists(path.join(clientDir, ".git"))) {
    yield { type: "phase", phase: "clone", message: "Repo déjà cloné — mise à jour (git fetch + reset)" }
    await runBinStream("git", ["-C", clientDir, "fetch", "origin"], { onLine: pushLog })
    while (logs.length) yield logs.shift()!
    await runBinStream("git", ["-C", clientDir, "reset", "--hard", "origin/main"], { onLine: pushLog })
    while (logs.length) yield logs.shift()!
  } else {
    yield { type: "phase", phase: "clone", message: `Clonage de ${ESNACK_REPO}` }
    // Stream each progress line from git as it arrives
    const clonePromise = runBinStream(
      "git",
      ["clone", "--depth", "1", "--progress", ESNACK_REPO, clientDir],
      { onLine: pushLog }
    )
    yield* pollLogs(logs, clonePromise)
  }

  yield { type: "phase", phase: "config", message: "Génération du .env depuis client_config" }
  // Use a placeholder URL initially — we'll overwrite .env with the real
  // ngrok URL and recreate api+frontend once the tunnel is up.
  const placeholderUrl = `http://localhost`
  await writeFile(path.join(clientDir, ".env"), buildEnvFile(client, config, placeholderUrl))
  await writeFile(path.join(clientDir, "docker-compose.override.yml"), buildOverrideCompose())
  yield { type: "log", message: ".env et docker-compose.override.yml écrits" }

  yield { type: "phase", phase: "build", message: "Build + démarrage de la stack (docker compose up --build)" }
  const upPromise = runBinStream(
    "docker",
    ["compose", "-p", project, "up", "-d", "--build"],
    { cwd: clientDir, onLine: pushLog }
  )
  yield* pollLogs(logs, upPromise)

  yield { type: "phase", phase: "ngrok", message: "Pull de ngrok/ngrok:latest puis lancement du tunnel" }
  const ngrokName = `${project}-ngrok`
  await stopAndRemoveIfExists(ngrokName)

  const docker = getDocker()
  await pullImage("ngrok/ngrok:latest")
  yield { type: "log", message: "Image ngrok/ngrok:latest prête" }

  const ngrok = await docker.createContainer({
    name: ngrokName,
    Image: "ngrok/ngrok:latest",
    Cmd: ["http", "frontend:3000", "--log=stdout"],
    Env: [`NGROK_AUTHTOKEN=${authtoken}`],
    Labels: {
      [MANAGED_LABEL]: "true",
      "klinkragency.client_id": client.id,
      "klinkragency.client_slug": client.slug,
      "klinkragency.role": "ngrok",
    },
    HostConfig: {
      NetworkMode: network,
      RestartPolicy: { Name: "unless-stopped" },
    },
    ExposedPorts: { "4040/tcp": {} },
  })
  await ngrok.start()
  yield { type: "log", message: `Container ${ngrokName} démarré` }

  yield { type: "phase", phase: "tunnel", message: "Attente de l'URL publique ngrok" }
  const publicUrl = await waitForNgrokUrl(ngrokName)
  yield { type: "log", message: `URL obtenue : ${publicUrl}` }

  // Rewrite .env with the real URL and recreate api+frontend so they read it
  yield { type: "phase", phase: "finalize", message: "Injection de PUBLIC_URL + recréation api/frontend" }
  await writeFile(path.join(clientDir, ".env"), buildEnvFile(client, config, publicUrl))
  const recreatePromise = runBinStream(
    "docker",
    ["compose", "-p", project, "up", "-d", "--force-recreate", "api", "frontend"],
    { cwd: clientDir, onLine: pushLog }
  )
  yield* pollLogs(logs, recreatePromise)

  await db
    .update(clients)
    .set({
      publicUrl,
      composeProject: project,
      ngrokContainerId: ngrok.id,
      status: "deployed",
      deployedAt: new Date(),
      updatedAt: new Date(),
    })
    .where(eq(clients.id, clientId))

  await recordProjectContainersInDb(project, client.id)

  yield { type: "done", publicUrl, composeProject: project }
}

/** Polls the logs buffer while a promise is pending, yielding events as they appear. */
async function* pollLogs(
  logs: DeployEvent[],
  promise: Promise<unknown>
): AsyncGenerator<DeployEvent> {
  let settled = false
  let error: Error | null = null
  promise.then(() => { settled = true }).catch((e) => { error = e instanceof Error ? e : new Error(String(e)); settled = true })
  while (!settled) {
    while (logs.length) yield logs.shift()!
    await new Promise((r) => setTimeout(r, 50))
  }
  while (logs.length) yield logs.shift()!
  if (error) throw error
}

/** Non-streaming stop for symmetry with the POST endpoint. Fast enough to be synchronous. */
export async function stopClientInstance(clientId: string): Promise<void> {
  const [client] = await db.select().from(clients).where(eq(clients.id, clientId)).limit(1)
  if (!client) throw new Error("Client not found")

  const project = client.composeProject || `esnack-${client.slug}`
  const clientDir = path.join(CLIENTS_DIR, client.slug)

  // Try the clean path first (compose down) if the compose file is still around
  if (await exists(path.join(clientDir, "docker-compose.yml"))) {
    try {
      await runBinStream(
        "docker",
        ["compose", "-p", project, "down", "-v", "--remove-orphans"],
        { cwd: clientDir, onLine: () => {} }
      )
    } catch (err) {
      console.warn("compose down failed, falling back to force cleanup:", err)
    }
  }

  // Always follow up with force cleanup — covers dead containers, networks,
  // volumes that compose missed, and handles the case where the directory
  // was deleted by hand.
  await forceCleanupProject(project)

  try {
    await rm(clientDir, { recursive: true, force: true })
  } catch {}

  await db.delete(managedContainers).where(eq(managedContainers.clientId, clientId))
  await db
    .update(clients)
    .set({
      publicUrl: null,
      composeProject: null,
      ngrokContainerId: null,
      status: "provisioned",
      updatedAt: new Date(),
    })
    .where(eq(clients.id, clientId))
}

/**
 * Kills every container / network / volume associated with a compose project
 * name, regardless of their state. Used for recovery from half-failed deploys.
 * Returns the total number of resources removed.
 */
export async function forceCleanupProject(project: string): Promise<number> {
  const docker = getDocker()
  let count = 0

  // 1. Containers (by label set by compose OR by name prefix — covers the
  //    case where a container exists by name but with no label, e.g. our
  //    ngrok sidecar that uses its own label).
  const containers = await docker.listContainers({
    all: true,
    filters: {
      label: [`com.docker.compose.project=${project}`],
    },
  })
  for (const c of containers) {
    try {
      await docker.getContainer(c.Id).remove({ force: true, v: true })
      count++
    } catch {}
  }
  // Also grab our ngrok sidecar by name
  try {
    await docker.getContainer(`${project}-ngrok`).remove({ force: true, v: true })
    count++
  } catch {}

  // 2. Networks named after the project (compose creates {project}_default)
  const networks = await docker.listNetworks({
    filters: { label: [`com.docker.compose.project=${project}`] },
  })
  for (const n of networks) {
    try {
      await docker.getNetwork(n.Id).remove()
      count++
    } catch {}
  }

  // 3. Volumes (postgres_data, redis_data, caddy_data, caddy_config)
  const volumes = await docker.listVolumes({
    filters: { label: [`com.docker.compose.project=${project}`] },
  })
  for (const v of volumes.Volumes || []) {
    try {
      await docker.getVolume(v.Name).remove()
      count++
    } catch {}
  }

  return count
}

async function stopAndRemoveIfExists(containerName: string): Promise<void> {
  const docker = getDocker()
  try {
    await docker.getContainer(containerName).remove({ force: true })
  } catch {
    // Not running / doesn't exist, ignore
  }
}

async function pullImage(image: string): Promise<void> {
  const docker = getDocker()
  await new Promise<void>((resolve, reject) => {
    docker.pull(image, (err: Error | null, stream: NodeJS.ReadableStream) => {
      if (err) return reject(err)
      docker.modem.followProgress(stream, (progressErr: Error | null) => {
        if (progressErr) return reject(progressErr)
        resolve()
      })
    })
  })
}

async function recordProjectContainersInDb(project: string, clientId: string): Promise<void> {
  const docker = getDocker()
  const list = await docker.listContainers({
    all: true,
    filters: { label: [`com.docker.compose.project=${project}`] },
  })
  for (const c of list) {
    const name = c.Names[0]?.replace(/^\//, "") ?? c.Id.slice(0, 12)
    try {
      await db
        .insert(managedContainers)
        .values({
          dockerName: name,
          image: c.Image,
          clientId,
        })
        .onConflictDoNothing()
    } catch {}
  }
}

/**
 * Polls the ngrok container's stdout for the "started tunnel … url=…" log line.
 * We shell out to `docker logs` (plain text, always) instead of dockerode's
 * Buffer API which returns stream-framed data that's awkward to decode.
 * The ngrok image ships no curl/wget so we can't hit its :4040 API either.
 */
async function waitForNgrokUrl(ngrokContainerName: string, timeoutMs = 30000): Promise<string> {
  const start = Date.now()
  const urlRegex = /url=(https:\/\/[^\s]+)/
  const errorRegex = /lvl=(warn|eror|crit)[^\n]*msg="([^"]+)"/

  while (Date.now() - start < timeoutMs) {
    try {
      const text = await captureLogs(ngrokContainerName)

      const m = text.match(urlRegex)
      if (m) return m[1]

      const err = text.match(errorRegex)
      if (err) throw new Error(`ngrok: ${err[2]}`)
    } catch (err) {
      if (err instanceof Error && err.message.startsWith("ngrok:")) throw err
      // ignore transient errors, retry
    }
    await new Promise((r) => setTimeout(r, 500))
  }
  throw new Error("Timed out waiting for ngrok public URL (check NGROK_AUTHTOKEN)")
}

function captureLogs(container: string): Promise<string> {
  return new Promise((resolve, reject) => {
    let out = ""
    const child = spawnProcess("docker", ["logs", container], { shell: false })
    child.stdout?.on("data", (d) => { out += d.toString() })
    child.stderr?.on("data", (d) => { out += d.toString() })
    child.on("error", reject)
    child.on("close", (code) => {
      if (code === 0) resolve(out)
      else reject(new Error(`docker logs exit ${code}`))
    })
  })
}
