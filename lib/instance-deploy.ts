export type DeployEvent =
  | { type: "phase"; phase: string; message: string }
  | { type: "log"; message: string }
  | { type: "error"; message: string }
  | { type: "done"; publicUrl: string; composeProject: string }

/** Runs a binary (no shell, no injection risk), streaming each stdout/stderr line via onLine. */
function runBinStream(
  cmd: string,
  args: string[],
  opts: { cwd?: string; env?: Record<string, string>; onLine: (line: string) => void } = { onLine: () => {} }
): Promise<void> {
  return new Promise((resolve, reject) => {
    const child = spawnProcess(cmd, args, {
      cwd: opts.cwd,
      env: { ...process.env, ...opts.env },
      shell: false,
    })
    let stderrTail = ""
    const emit = (buf: Buffer | string) => {
      buf.toString().split("\n").forEach((l) => {
        if (l.length > 0) opts.onLine(l)
      })
    }
    child.stdout?.on("data", emit)
    child.stderr?.on("data", (b: Buffer) => {
      stderrTail += b.toString()
      emit(b)
    })
    child.on("error", reject)
    child.on("close", (code) => {
      if (code === 0) resolve()
      else reject(new Error(`${cmd} exit ${code}: ${stderrTail.slice(-400)}`))
    })
  })
}

